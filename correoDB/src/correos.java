    public void llenarTablaClientes() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/empresa", "root", "");
            Statement stmt = con.createStatement();

            // Ejecutar la consulta y obtener los resultados en un objeto ResultSet
            String query = "SELECT * FROM Clientes";
            ResultSet resultSet = stmt.executeQuery(query);

            // Obtener el modelo de tabla
            DefaultTableModel model = (DefaultTableModel) tbcontactos.getModel();

            // Limpiar la tabla antes de llenarla con nuevos datos
            model.setRowCount(0);

            // Iterar sobre los resultados del ResultSet y agregar cada fila al modelo de la tabla
            while (resultSet.next()) {
                Object[] row = new Object[5];
                row[0] = resultSet.getInt("ID");
                row[1] = resultSet.getString("NombreCompleto");
                row[2] = resultSet.getString("CorreoElectronico");
                row[3] = resultSet.getString("Telefono");
                row[4] = resultSet.getString("Direccion");
                model.addRow(row);
            }
            // Cerrar la conexi√≥n y el Statement
            stmt.close();
            con.close();
        } catch (SQLException e) {
            System.err.println("Error al conectar a la base de datos: " + e.getMessage());
        }
    }

    public void enviarCorreos() {
        String host = "smtp.gmail.com"; // Cambia esto al servidor SMTP correspondiente
        int port = 587; // Cambia esto al puerto SMTP adecuado

        Properties props = new Properties();
        props.put("mail.smtp.host", host);
        props.put("mail.smtp.port", port);
        props.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.mail.sender", "username");
        props.put("mail.smtp.starttls.enable", "true");

        String username = txde.getText();
        String password = new String(txpass.getPassword());
        String saludo = txsaludo.getText();
        String asunto = txasunto.getText();

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/empresa", "root", "");
            Statement stmt = con.createStatement();

            String query = "SELECT CorreoElectronico, NombreCompleto FROM Clientes";
            ResultSet resultSet = stmt.executeQuery(query);

            while (resultSet.next()) {
                String recipient = resultSet.getString("CorreoElectronico");
                String nombre = resultSet.getString("NombreCompleto");

                Session session = Session.getInstance(props, new Authenticator() {
                    @Override
                    protected PasswordAuthentication getPasswordAuthentication() {
                        return new PasswordAuthentication(username, password);
                    }
                });

                Message message = new MimeMessage(session);
                message.setFrom(new InternetAddress(username));
                message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(recipient));
                message.setSubject(saludo + " - " + asunto);
                message.setText("Hola " + nombre + ",\n\nEste es un mensaje personalizado para ti.");

                Transport.send(message);

                System.out.println("Correo enviado a: " + recipient);
            }

            stmt.close();
            con.close();
        } catch (SQLException | MessagingException e) {
            e.printStackTrace();
        }
    }
