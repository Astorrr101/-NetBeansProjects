    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        // TODO add your handling code here:
        String myquery = "";
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/matricula","root","");
            stmt = con.createStatement();
            System.out.println("Conexi√≥n exitosa a la base de datos");
            myquery = "UPDATE Alumno SET AlumnoName = '" + txname.getText() + "', AlumnoTel = '" + txtele.getText() + "', AlumnoDir = '" + txdire.getText() + "', AlumnoEmail = '" + txemail.getText() + "', AlumnoGene = '" + cbgene.getSelectedItem() + "' WHERE AlumnoCode = '" + txcode.getText() + "'";
            int rowsAffected = stmt.executeUpdate(myquery);
            System.out.println(rowsAffected + " fila(s) actualizada(s) correctamente");
            con.close();
        } catch (SQLException e) {
            System.err.println("Error al conectar a la base de datos: " + e.getMessage());
        }
        limpiar();
    }//GEN-LAST:event_btsaveActionPerformed

    private void btbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbuscarActionPerformed
        // TODO add your handling code here:
        
        num_reg=1;
        cant_reg=1;
        try{
            myquery1="select count(*) as cantidad from alumno where "+cbcelda.getSelectedItem() +"='"+txcode.getText()+"'";
            myquery2="select * from alumno where "+cbcelda.getSelectedItem()+"='"+txcode.getText()+"'";
            rs1 = st.executeQuery(myquery1);
            rs1.next();
            cant_reg=Integer.parseInt(rs1.getString("cantidad"));
            txregis.setText(""+num_reg);
            txde.setText(""+cant_reg);
            rs1.close();
            rs2=st.executeQuery(myquery2);
            rs2.next();
            txcodigo.setText(""+rs2.getInt("AlumnoCode"));
            txname.setText(rs2.getString("AlumnoName"));
            txtele.setText(rs2.getString("AlumnoTel"));
            txdire.setText(rs2.getString("AlumnoDir"));
            txemail.setText(rs2.getString("AlumnoEmail"));
            cbgene.addItem(rs2.getString("AlumnoGene"));
        }catch (Exception e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_btbuscarActionPerformed

