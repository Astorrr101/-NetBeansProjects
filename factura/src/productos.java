public void seleccionarFila() {
        int indiceFilaSeleccionada = tbproductos.getSelectedRow();

        if (indiceFilaSeleccionada >= 0) {
            DefaultTableModel modeloTabla = (DefaultTableModel) tbproductos.getModel();
            String codigo = modeloTabla.getValueAt(indiceFilaSeleccionada, 0).toString();
            String descripcion = modeloTabla.getValueAt(indiceFilaSeleccionada, 1).toString();
            String precio = modeloTabla.getValueAt(indiceFilaSeleccionada, 2).toString();
            String cantidad = modeloTabla.getValueAt(indiceFilaSeleccionada, 3).toString();

            // Enviar los datos al formulario de factura
            enviarDatosAFactura(codigo, descripcion, precio, cantidad);
        }
    }


    public void llenarTabla() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/facturacion", "root", "");
            Statement stmt = con.createStatement();

            // Ejecutar la consulta y obtener los resultados en un objeto ResultSet
            String query = "SELECT * FROM productos";
            ResultSet resultSet = stmt.executeQuery(query);

            // Obtener el modelo de tabla
            DefaultTableModel model = (DefaultTableModel) tbproductos.getModel();

            // Limpiar la tabla antes de llenarla con nuevos datos
            model.setRowCount(0);

            // Iterar sobre los resultados del ResultSet y agregar cada fila al modelo de la tabla
            while (resultSet.next()) {
                Object[] row = new Object[4];
                row[0] = resultSet.getInt("codigo");
                row[1] = resultSet.getString("description");
                row[2] = resultSet.getString("precio");
                row[3] = resultSet.getString("cantidad");
                model.addRow(row);
            }
            // Cerrar la conexi√≥n y el Statement

            stmt.close();
            con.close();
        } catch (SQLException e) {
            System.err.println("Error al conectar a la base de datos: " + e.getMessage());
        }
    }
